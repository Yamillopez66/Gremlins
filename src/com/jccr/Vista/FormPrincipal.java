//METODO QUE DEVUELVE UN VALOR BOOLEAN PARA SABER SI UN JINTERNALFRAME ESTA ABIERTO O NO
public boolean verificarVentana(Object obj){
    JInternalFrame[] activos = this.jDesktopPanelPrincipal.getAllFrames();
    boolean cerrado = true;
    int i = 0;
    while (i<activos.length && cerrado){
        if(activos[i] == obj){
            cerrado = false;
        }
        i++;
    }
    return cerrado;
}

